import yfinance as yf
import pandas as pd
from ta.momentum import RSIIndicator
from ta.trend import MACD, SMAIndicator
from ta.volatility import BollingerBands

from datetime import date, timedelta
from ta.volatility import BollingerBands

tickers = ['GOOGL', 'META', 'NVDA', 'TSLA']

all_data = []

start_date = date.today() - timedelta(days=365*5.2)

for ticker in tickers:
    # 1. Download data
    df = yf.download(ticker, start = start_date)

    # 2. FLATTEN THE MULTIINDEX
    # This turns the nested 'Price/Ticker' columns into simple single columns
    df.columns = df.columns.get_level_values(0)

    # 3. Add the Ticker column
    df['Ticker'] = ticker

    # Now df['Close'] is a standard Series, and your indicators will work perfectly
    close_prices = df['Close']

    # --- Your existing indicator logic ---
    df['RSI'] = RSIIndicator(close=close_prices, window=14).rsi()
    macd = MACD(close=close_prices)
    df['MACD'] = macd.macd()
    df['MACD_signal'] = macd.macd_signal()

    # Initialize Bollinger Bands Indicator
    indicator_bb = BollingerBands(close=df['Close'], window=20, window_dev=2)

    # Add bands to your dataframe
    df['BB_High'] = indicator_bb.bollinger_hband()
    df['BB_Low'] = indicator_bb.bollinger_lband()
    df['BB_Mid'] = indicator_bb.bollinger_mavg() # The SMA 20

    # 4. Reset index so 'Date' becomes a regular column
    all_data.append(df.reset_index())

# 5. Combine everything into one long table
final_df = pd.concat(all_data, ignore_index=True)

# Ensure columns are a single level index after concatenation
# The kernel state output and KeyError suggest that `final_df` might have MultiIndex columns after concat.
# For example, ('Price', 'Close') or ('Price', 'Ticker').
# This line flattens the columns by dropping the top level.
# It's safer to ensure this after concat as `yf.download` and `pd.concat` can sometimes lead to MultiIndex.
if isinstance(final_df.columns, pd.MultiIndex):
    final_df.columns = final_df.columns.droplevel(0)

# Filter out rows with any NaN values
final_df = final_df.dropna()

