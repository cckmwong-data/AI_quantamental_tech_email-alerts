# Create a function to apply to each ticker group
def add_signals(df):
    # 1. MACD Crossover Signals
    # Buy when MACD crosses above Signal; Sell when it crosses below
    df['MACD_Action'] = 'Hold'
    df.loc[(df['MACD'] > df['MACD_signal']) & (df['MACD'].shift(1) <= df['MACD_signal'].shift(1)), 'MACD_Action'] = 'Buy'
    df.loc[(df['MACD'] < df['MACD_signal']) & (df['MACD'].shift(1) >= df['MACD_signal'].shift(1)), 'MACD_Action'] = 'Sell'

    # 2. RSI Threshold Signals
    df['RSI_Action'] = 'Hold'
    df.loc[df['RSI'] < 30, 'RSI_Action'] = 'Buy'
    df.loc[df['RSI'] > 70, 'RSI_Action'] = 'Sell'

    # 3. Bollinger Band Signals (Mean Reversion Strategy)
    df['BB_Action'] = 'Hold'
    # Buy when price closes below the Lower Band
    df.loc[df['Close'] <= df['BB_Low'], 'BB_Action'] = 'Buy'
    # Sell when price closes above the Upper Band
    df.loc[df['Close'] >= df['BB_High'], 'BB_Action'] = 'Sell'

    return df

# Apply the signals to each ticker separately
final_df = final_df.groupby('Ticker', group_keys=False).apply(add_signals)

