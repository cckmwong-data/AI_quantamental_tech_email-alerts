def get_ticker_comments(df):
    # 1. Identify unique tickers in the dataset
    tickers = df['Ticker'].unique()

    # List to store our final results
    results = []

    # Using the latest stable model for 2026
    model = genai.GenerativeModel('gemini-2.5-flash')

    print(f"Starting analysis for {len(tickers)} tickers...")

    for ticker in tickers:
        # 2. Get the latest data point for THIS specific ticker
        ticker_data = df[df['Ticker'] == ticker].iloc[-1]

        # Prepare a focused prompt for a single ticker
        prompt = f"""
        You are a Senior Quantitative Analyst. Analyze the technical state of {ticker}:
        - Price: ${ticker_data['Close']:.2f}
        - RSI: {ticker_data['RSI']:.2f} ({ticker_data['RSI_Action']})
        - MACD: {ticker_data['MACD']:.3f} (Signal: {ticker_data['MACD_signal']:.3f})
        - MACD Action: {ticker_data['MACD_Action']}
        - BB High: {ticker_data['BB_High']}
        - BB Mid: {ticker_data['BB_Mid']}
        - BB Low: {ticker_data['BB_Low']}

        Provide a 2-sentence technical summary within 100 words.
        """

        try:
            # 3. Generate comment for this ticker
            response = model.generate_content(prompt)
            comment = response.text.strip()

            # Append to results list
            results.append({
                'Ticker': ticker,
                'Comment': comment
            })
            print(f"Processed: {ticker}")

        except Exception as e:
            print(f"Error analyzing {ticker}: {str(e)}")
            results.append({'Ticker': ticker, 'Comment': "Analysis failed."})

    # 4. Convert the results list into a new DataFrame (Table)
    analysis_table = pd.DataFrame(results)
    return analysis_table

# Execute the analysis
